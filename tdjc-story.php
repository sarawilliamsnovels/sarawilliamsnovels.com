				<center><h3>The story behind `The Don Juan Con`</h3></center><br>

				<p style='text-indent:40px'>I was a newspaper reporter for the Fort Myers, Florida News-Press when a woman walked into our newsroom one day and said shed been romanced out of money she desperately needed to pay her bills and support her children. As it turned out, shed been victimized by a specialized type of con artist, a sweetheart swindler. After months of linking with other reporters and police detectives across the South, we were able to jail the swindler. In most cases, thanks to timid victims and an apathetic justice system, these con artists are free to perpetuate their vicious schemes.</p>
				<p style='text-indent:40px'>Most victims of sweetheart swindlers hide their heads in shame. Some of them commit suicide. So few sweetheart swindlers are prosecuted that there are no statistics available on a national basis. Yet this is such a common scam that I cant do a book signing without hearing another story.</p>
				<p style='text-indent:40px'>It is a very common misconception that only foolish and naive women can be taken advantage of in this manner. Yet very sophisticated, high profile women have been victims: a prominent U.S. senator, the woman who was the mayor of Mexico City, and a television anchorwoman in a major U.S. market have all been romanced out of their money, and in some cases, their careers.</p>
				<p style='text-indent:40px'>The Don Juan Con may be a page turner of a novel, but long ago, in my early days of fiction, I learned from a wise old publisher that a novelists role is not to teach but to entertain. Victims can get their revenge by reading Don Juan; I surely hope they do. But I really wrote The Don Juan Con to raise awareness. Im as much a believer in love at first sight as anyone; but on the other hand women swept off their feet need to pay attention. They have to avoid mixing up their emotions with their bank accounts. These days, to my great satisfaction, a number of friends or family members of someone whom they suspect is being romanced by a con artist have sent copies of The Don Juan Con to the potential victim in the form of a wakeup call.</p>
				<p style='text-indent:40px'>One of the pleasures of writing The Don Juan Con was linking up with a Milwaukee detective, Dennis Marlock. This retired detective spearheads a group of professionals in the justice system lobbying for more professional training about sweetheart scam artists and other bunko criminals. Marlock says that for a lack of training, sensitivity and awareness, the justice system simply doesnt give victims an even break. Please note Detective Marlocks remarks on the cover of The Don Juan Con.</p>
				<p style='text-indent:40px'>Meanwhile, detective Marlock says he hears on average a new case per day, so if  someone you know is on the verge of being victimized in this manner, please e-mail me or go to Marlocks website:</p>
